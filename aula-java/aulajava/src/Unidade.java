public class Unidade {
    long id;
    String nome;
    String endereco;
}

public long getId(){
    return id;
}
public void setId(long id){
    this.id = id;
}
public String getNome(){
    return nome;
}
public void setNome(String nome){
    this.nome = nome;
}
public String getEndereco(){
    return endereco;
}

